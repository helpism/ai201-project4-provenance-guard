# ---------------------------------------------------------------------------
# Signal 1 — LLM Holistic Analysis
# ---------------------------------------------------------------------------
LLM_SYSTEM_PROMPT = (
    "You are a forensic text-provenance classifier. Assess the holistic "
    "semantic structure, predictability, and phrase transitions of the text "
    "the user provides and estimate the likelihood that it was generated by "
    "an AI language model.\n\n"
    "Respond with EXACTLY ONE number: a decimal float between 0.0 and 1.0, "
    "where 0.0 means highly likely human-written and 1.0 means highly likely "
    "AI-generated. Output ONLY the number. Do NOT include any words, labels, "
    "punctuation, explanation, units, or formatting. For example: 0.73"
)


def _coerce_float(raw: str) -> float:
    """Safely extract a 0.0–1.0 float from an LLM response string.

    The system prompt asks for a bare float, but we never trust that blindly.
    We pull the first number out of the response and clamp it to [0.0, 1.0]
    so a chatty or malformed reply can never break the JSON serializer.
    """
    match = re.search(r"[-+]?\d*\.?\d+", raw or "")
    if not match:
        return 0.0
    try:
        value = float(match.group())
    except (ValueError, TypeError):
        return 0.0
    return max(0.0, min(1.0, value))


def analyze_with_llm(text: str) -> float:
    """Signal 1: ask Groq's llama-3.3-70b-versatile for an AI-likelihood score.

    Returns a float in [0.0, 1.0]. Never raises on a malformed model reply —
    that case degrades to 0.0 via _coerce_float.
    """
    client = get_groq_client()
    completion = client.chat.completions.create(
        model=GROQ_MODEL,
        messages=[
            {"role": "system", "content": LLM_SYSTEM_PROMPT},
            {"role": "user", "content": text},
        ],
        temperature=0.0,  # deterministic scoring
        max_tokens=10,    # we only need a short number back
    )
    raw = completion.choices[0].message.content.strip()
    return _coerce_float(raw)


# ---------------------------------------------------------------------------
# SQLite Audit Log
# ---------------------------------------------------------------------------
def get_db():
    """Open a fresh connection per request. Caller is responsible for close().

    sqlite3.Row gives us dict-like access for easy JSON serialization.
    """
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    return conn


def init_db():
    """Create the audit_log table if it doesn't already exist."""
    conn = get_db()
    try:
        conn.execute(
            """
            CREATE TABLE IF NOT EXISTS audit_log (
                id          INTEGER PRIMARY KEY AUTOINCREMENT,
                timestamp   TEXT    NOT NULL,
                content_id  TEXT    NOT NULL UNIQUE,
                creator_id  TEXT    NOT NULL,
                llm_score   REAL    NOT NULL,
                confidence  REAL    NOT NULL DEFAULT 0.0,
                attribution TEXT    NOT NULL DEFAULT 'pending',
                status      TEXT    NOT NULL DEFAULT 'classified'
            )
            """
        )
        conn.commit()
    finally:
        conn.close()


def insert_record(content_id, creator_id, llm_score,
                  confidence=0.0, attribution="pending", status="classified"):
    """Insert a single audit-log row and return the stored record as a dict."""
    timestamp = datetime.now(timezone.utc).isoformat()
    conn = get_db()
    try:
        conn.execute(
            """
            INSERT INTO audit_log
                (timestamp, content_id, creator_id, llm_score,
                 confidence, attribution, status)
            VALUES (?, ?, ?, ?, ?, ?, ?)
            """,
            (timestamp, content_id, creator_id, llm_score,
             confidence, attribution, status),
        )
        conn.commit()
    finally:
        conn.close()

    return {
        "timestamp": timestamp,
        "content_id": content_id,
        "creator_id": creator_id,
        "llm_score": llm_score,
        "confidence": confidence,
        "attribution": attribution,
        "status": status,
    }


