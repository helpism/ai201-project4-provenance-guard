@app.route("/submit", methods=["POST"])
# @limiter.limit("10/minute")  # FUTURE: enable once the rate limiter is wired
def submit():
    """Accept {text, creator_id}, score via Signal 1, log, and return JSON."""
    data = request.get_json(silent=True) or {}
    text = data.get("text")
    creator_id = data.get("creator_id")

    if not text or not isinstance(text, str) or not text.strip():
        return jsonify({"error": "Field 'text' is required and must be a non-empty string."}), 400
    if not creator_id:
        return jsonify({"error": "Field 'creator_id' is required."}), 400

    content_id = str(uuid.uuid4())

    try:
        llm_score = analyze_with_llm(text)
    except Exception as exc:  # network / API / config failure
        return jsonify({"error": f"LLM analysis failed: {exc}"}), 502

    record = insert_record(
        content_id=content_id,
        creator_id=creator_id,
        llm_score=llm_score,
        confidence=0.0,         # placeholder until ensemble engine (M4)
        attribution="pending",  # placeholder until label logic (M5)
        status="classified",
    )

    return jsonify(record), 201


@app.route("/log", methods=["GET"])
def get_log():
    """Return the most recent audit-log entries as JSON.

    Optional ?limit=N query param (defaults to 20, capped at 100).
    """
    try:
        limit = int(request.args.get("limit", 20))
    except (TypeError, ValueError):
        limit = 20
    limit = max(1, min(limit, 100))

    conn = get_db()
    try:
        rows = conn.execute(
            "SELECT * FROM audit_log ORDER BY id DESC LIMIT ?",
            (limit,),
        ).fetchall()
    finally:
        conn.close()

    return jsonify([dict(row) for row in rows]), 200


