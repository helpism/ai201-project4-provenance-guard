# A single reusable Groq client. Instantiated lazily so the app can still
# boot (and serve /log) even if the key is missing during local dev.
_groq_client = None


def get_groq_client():
    """Return a cached Groq client, creating it on first use."""
    global _groq_client
    if _groq_client is None:
        if not GROQ_API_KEY:
            raise RuntimeError("GROQ_API_KEY is not set. Add it to your .env file.")
        _groq_client = Groq(api_key=GROQ_API_KEY)
    return _groq_client


